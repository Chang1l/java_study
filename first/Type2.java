package first;

public class Type2 {

	public static void main(String[] args) {
		float value1 = 3;
		long value2 = 5;
		double value3 = 7;
		
		System.out.println(value1);
		System.out.println(value2);
		System.out.println(value3);
		System.out.println();
		
		int value4 = (int)3.5;
		float value5 = (float)7.5;
		
		System.out.println(value4);
		System.out.println(value5);
	    
	}

}
